def detect_field_lines(frame):
    """
    Detecta las l√≠neas del campo usando Canny + Hough.
    Devuelve una lista de segmentos de l√≠nea.
    """
    gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
    blur = cv2.GaussianBlur(gray, (5,5), 0)
    edges = cv2.Canny(blur, 50, 150, apertureSize=3)

    lines = cv2.HoughLinesP(edges, 1, np.pi/180, threshold=120,
                            minLineLength=80, maxLineGap=10)
    if lines is None:
        return []
    return lines[:,0]  # (x1,y1,x2,y2)


def get_court_keypoints(lines):
    """
    A partir de las l√≠neas detectadas intenta obtener puntos de la cancha.
    Para simplificar: devuelve esquinas aproximadas (solo si hay suficientes l√≠neas).
    """
    if len(lines) < 4:
        return None

    # Extraer todos los extremos
    pts = []
    for (x1,y1,x2,y2) in lines:
        pts.append((x1,y1))
        pts.append((x2,y2))

    # Convex hull para aproximar contorno de la cancha
    pts = np.array(pts)
    hull = cv2.convexHull(pts)
    if len(hull) < 4:
        return None

    # Tomamos 4 puntos principales (esquinas del hull)
    hull = hull.squeeze()
    rect = cv2.boundingRect(hull)
    x,y,w,h = rect
    approx_court = np.array([
        [x,y], [x+w,y],
        [x+w,y+h], [x,y+h]
    ], dtype=np.float32)

    return approx_court


def get_reference_court():
    """
    Define el rect√°ngulo de referencia del campo en el plano 2D (homograf√≠a destino).
    """
    return np.array([
        [0,0],
        [FIELD_W_PX,0],
        [FIELD_W_PX,FIELD_H_PX],
        [0,FIELD_H_PX]
    ], dtype=np.float32)

def pixel_to_field_homography(x, y, H):
    """
    Transforma un punto (x,y) de la imagen al plano del campo usando homograf√≠a H.
    """
    pt = np.array([[x,y]], dtype=np.float32)
    pt = np.array([pt])
    dst = cv2.perspectiveTransform(pt, H)
    return int(dst[0,0,0]), int(dst[0,0,1])

